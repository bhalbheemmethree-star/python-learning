# break and continue in python

for i in range(14):
    if i == 10:
        print("breaking the loop")
        break
    print("5 X",i+1,"=",5*(i+1))




for i in range(14):
    if i == 10:
        print("skip the iteration")
        continue
    print("5 X",i,"=",5*i)


